package activity;


public class wake_up extends Engine{

	protected int wakeup_id;
	protected int wakeup_timing;
	protected String wakeup_description;
	
	public int getWakeup_id() {
		return wakeup_id;
	}
	public void setWakeup_id(int wakeup_id) {
		this.wakeup_id = wakeup_id;
	}
	public int getWakeup_timing() {
		return wakeup_timing;
	}
	public void setWakeup_timing(int wakeup_timing) {
		this.wakeup_timing = wakeup_timing;
	}
	public String getWakeup_description() {
		return wakeup_description;
	}
	public void setWakeup_description(String wakeup_description) {
		this.wakeup_description = wakeup_description;
	}
	
}
